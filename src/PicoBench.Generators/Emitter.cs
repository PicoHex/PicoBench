namespace PicoBench.Generators;

/// <summary>
/// Generates the partial class source code implementing <c>IBenchmarkClass.RunBenchmarks</c>.
/// All types in emitted code use <c>global::</c> qualification to avoid namespace conflicts.
/// </summary>
internal static class Emitter
{
    private const string Bench = "global::PicoBench";
    private const string SystemAction = "global::System.Action";

    public static string Generate(BenchmarkClassModel model)
    {
        var sb = new StringBuilder();
        var hasParams = model.ParamsProperties.Length > 0;
        var hasIterSetup = model.IterationSetupMethod is not null;
        var hasIterCleanup = model.IterationCleanupMethod is not null;

        // ── File header ──────────────────────────────────────────────
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("// Generated by PicoBench.Generators — do not edit.");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();

        // ── Namespace ────────────────────────────────────────────────
        var indent = "";
        if (model.Namespace is not null)
        {
            sb.AppendLine($"namespace {model.Namespace}");
            sb.AppendLine("{");
            indent = "    ";
        }

        // ── Partial class + interface ────────────────────────────────
        sb.AppendLine(
            $"{indent}{model.AccessModifier} partial class {model.ClassName} : {Bench}.IBenchmarkClass"
        );
        sb.AppendLine($"{indent}{{");

        var m1 = indent + "    "; // method indent
        var m2 = m1 + "    "; // body indent

        // ── RunBenchmarks method ─────────────────────────────────────
        sb.AppendLine($"{m1}/// <inheritdoc/>");
        sb.AppendLine(
            $"{m1}public {Bench}.BenchmarkSuite RunBenchmarks({Bench}.BenchmarkConfig? config = null)"
        );
        sb.AppendLine($"{m1}{{");

        sb.AppendLine($"{m2}config ??= {Bench}.BenchmarkConfig.Default;");
        sb.AppendLine(
            $"{m2}var __results = new global::System.Collections.Generic.List<{Bench}.BenchmarkResult>();"
        );
        sb.AppendLine(
            $"{m2}var __comparisons = new global::System.Collections.Generic.List<{Bench}.ComparisonResult>();"
        );
        sb.AppendLine($"{m2}var __sw = global::System.Diagnostics.Stopwatch.StartNew();");
        sb.AppendLine();

        // ── [Params] loops ───────────────────────────────────────────
        var bodyIndent = m2;
        if (hasParams)
        {
            foreach (var p in model.ParamsProperties)
            {
                var valuesLiteral = string.Join(", ", p.FormattedValues);
                sb.AppendLine(
                    $"{bodyIndent}foreach (var __p_{p.Name} in new {p.TypeFullName}[] {{ {valuesLiteral} }})"
                );
                sb.AppendLine($"{bodyIndent}{{");
                bodyIndent += "    ";
                sb.AppendLine($"{bodyIndent}this.{p.Name} = __p_{p.Name};");
            }

            // Build the combined parameter label: "N=10, Mode=A"
            var labelParts = model.ParamsProperties.Select(p => $"{p.Name}={{__p_{p.Name}}}");
            sb.AppendLine($"{bodyIndent}var __paramLabel = $\"{string.Join(", ", labelParts)}\";");
            sb.AppendLine();
        }

        // ── GlobalSetup ──────────────────────────────────────────────
        if (model.GlobalSetupMethod is not null)
        {
            sb.AppendLine($"{bodyIndent}this.{model.GlobalSetupMethod}();");
            sb.AppendLine();
        }

        // ── Benchmark methods ────────────────────────────────────────
        foreach (var method in model.Methods)
        {
            var nameExpr = hasParams
                ? $"$\"{method.Name} [{{__paramLabel}}]\""
                : $"\"{method.Name}\"";

            if (hasIterSetup || hasIterCleanup)
            {
                // Need the full Benchmark.Run overload with setup/teardown
                sb.AppendLine(
                    $"{bodyIndent}{SystemAction} __action_{method.Name} = () => this.{method.Name}();"
                );

                var setupArg = hasIterSetup
                    ? $"({SystemAction})(() => this.{model.IterationSetupMethod}())"
                    : "null";
                var teardownArg = hasIterCleanup
                    ? $"({SystemAction})(() => this.{model.IterationCleanupMethod}())"
                    : "null";

                sb.AppendLine($"{bodyIndent}var __r_{method.Name} = {Bench}.Benchmark.Run(");
                sb.AppendLine($"{bodyIndent}    {nameExpr},");
                sb.AppendLine($"{bodyIndent}    __action_{method.Name},");
                sb.AppendLine($"{bodyIndent}    warmup: __action_{method.Name},");
                sb.AppendLine($"{bodyIndent}    config: config,");
                sb.AppendLine($"{bodyIndent}    setup: {setupArg},");
                sb.AppendLine($"{bodyIndent}    teardown: {teardownArg});");
            }
            else
            {
                sb.AppendLine(
                    $"{bodyIndent}var __r_{method.Name} = {Bench}.Benchmark.Run({nameExpr}, () => this.{method.Name}(), config);"
                );
            }

            sb.AppendLine($"{bodyIndent}__results.Add(__r_{method.Name});");
            sb.AppendLine();
        }

        // ── Comparisons (if a baseline exists) ──────────────────────
        var baselineMethod = model.Methods.FirstOrDefault(m => m.IsBaseline);
        if (baselineMethod is not null)
        {
            foreach (var method in model.Methods)
            {
                if (method.IsBaseline)
                    continue;

                var compNameExpr = hasParams
                    ? $"$\"{method.Name} vs {baselineMethod.Name} [{{__paramLabel}}]\""
                    : $"\"{method.Name} vs {baselineMethod.Name}\"";
                var categoryExpr = hasParams ? "__paramLabel" : "null";

                sb.AppendLine(
                    $"{bodyIndent}__comparisons.Add(new {Bench}.ComparisonResult({compNameExpr}, __r_{baselineMethod.Name}, __r_{method.Name}, {categoryExpr}));"
                );
            }
            sb.AppendLine();
        }

        // ── GlobalCleanup ────────────────────────────────────────────
        if (model.GlobalCleanupMethod is not null)
        {
            sb.AppendLine($"{bodyIndent}this.{model.GlobalCleanupMethod}();");
            sb.AppendLine();
        }

        // ── Close [Params] loops ─────────────────────────────────────
        if (hasParams)
        {
            for (var i = model.ParamsProperties.Length - 1; i >= 0; i--)
            {
                bodyIndent = bodyIndent.Substring(0, bodyIndent.Length - 4);
                sb.AppendLine($"{bodyIndent}}}");
            }
            sb.AppendLine();
        }

        // ── Return BenchmarkSuite ────────────────────────────────────
        var descExpr = model.Description is not null
            ? EscapeStringLiteral(model.Description)
            : "null";

        sb.AppendLine($"{m2}__sw.Stop();");
        sb.AppendLine($"{m2}return new {Bench}.BenchmarkSuite(");
        sb.AppendLine($"{m2}    \"{EscapeRaw(model.ClassName)}\",");
        sb.AppendLine($"{m2}    new {Bench}.EnvironmentInfo(),");
        sb.AppendLine($"{m2}    __results,");
        sb.AppendLine($"{m2}    __sw.Elapsed,");
        sb.AppendLine($"{m2}    description: {descExpr},");
        sb.AppendLine($"{m2}    comparisons: __comparisons);");

        // ── Close method / class / namespace ─────────────────────────
        sb.AppendLine($"{m1}}}");
        sb.AppendLine($"{indent}}}");

        if (model.Namespace is not null)
            sb.AppendLine("}");

        return sb.ToString();
    }

    private static string EscapeStringLiteral(string s) => $"\"{EscapeRaw(s)}\"";

    private static string EscapeRaw(string s) =>
        s.Replace("\\", "\\\\")
            .Replace("\"", "\\\"")
            .Replace("\r", "\\r")
            .Replace("\n", "\\n")
            .Replace("\t", "\\t");
}
