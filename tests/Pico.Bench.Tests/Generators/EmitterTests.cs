    // ─── Combined scenario ──────────────────────────────────────────

    [Test]
    [Property("Category", "Emitter")]
    public async Task Generate_FullModel_ContainsAllExpectedParts()
    {
        var methods = ImmutableArray.Create(
            new BenchmarkMethodModel { Name = "Baseline", IsBaseline = true },
            new BenchmarkMethodModel { Name = "Candidate", IsBaseline = false }
        );
        var paramsProps = ImmutableArray.Create(
            new ParamsPropertyModel
            {
                Name = "N",
                TypeFullName = "int",
                FormattedValues = ImmutableArray.Create("10", "100"),
            }
        );
        var code = Emitter.Generate(
            MinimalModel(
                ns: "My.Namespace",
                description: "Full test",
                globalSetup: "Setup",
                globalCleanup: "Cleanup",
                iterSetup: "IterSetup",
                iterCleanup: "IterCleanup",
                methods: methods,
                paramsProps: paramsProps
            )
        );

        await Assert.That(code).Contains("namespace My.Namespace");
        await Assert.That(code).Contains("this.Setup();");
        await Assert.That(code).Contains("this.Cleanup();");
        await Assert.That(code).Contains("setup: (global::System.Action)(() => this.IterSetup())");
        await Assert
            .That(code)
            .Contains("teardown: (global::System.Action)(() => this.IterCleanup())");
        await Assert.That(code).Contains("foreach (var __p_N in new int[] { 10, 100 })");
        await Assert.That(code).Contains("Candidate vs Baseline");
        await Assert.That(code).Contains("description: \"Full test\"");
    }

