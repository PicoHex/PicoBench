    [Test]
    [Property("Category", "Emitter")]
    public async Task Generate_MultipleMethods_RunsAll()
    {
        var methods = ImmutableArray.Create(
            new BenchmarkMethodModel { Name = "MethodA", IsBaseline = false },
            new BenchmarkMethodModel { Name = "MethodB", IsBaseline = false }
        );
        var code = Emitter.Generate(MinimalModel(methods: methods));

        await Assert.That(code).Contains("this.MethodA()");
        await Assert.That(code).Contains("this.MethodB()");
        await Assert.That(code).Contains("__results.Add(__r_MethodA)");
        await Assert.That(code).Contains("__results.Add(__r_MethodB)");
    }

